<p>
    Sebelum peresmian, asrama telah terlebih dahulu dihuni oleh para mahasiswa. Proses pengundian kamar dan pembagian teman sekamar dilakukan di Sekretariat KPMB di kawasan Tello. Pada awal masa huni, lantai dua gedung putra telah terisi penuh, sedangkan gedung putri masih dalam tahap pemenuhan penghuni.
</p>

<p>
    Dalam hal tata kelola, Asrama Mahasiswa Balikpapan mengadopsi sistem dan tata tertib yang merujuk pada aturan Organda Sulawesi serta GBHO | VG, dengan sistem operasional awal dirintis oleh Kak Nurul. Sumber dana operasional pada masa awal berasal dari hibah Pemerintah Kota Balikpapan sebesar Rp100 juta yang diberikan untuk dua tahun pertama. Namun, seiring perubahan kebijakan nasional melalui Kementerian Dalam Negeri terkait regulasi dana hibah, pendanaan tersebut dihentikan pada tahun kedua.
</p>

<p>
    Pada masa awal pendirian, asrama belum memiliki struktur organisasi formal seperti MOAS (Musyawarah Asrama). Mekanisme penerimaan penghuni dilakukan secara langsung dan disertai dengan pelaksanaan Latihan Dasar Kepemimpinan (LDK) sebagai bagian dari proses pengenalan dan pembinaan awal.
</p>

<p>
