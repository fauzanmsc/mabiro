    Didirikan pada tanggal 7 Desember 2007, asrama ini diresmikan melalui sebuah seremonial yang diselenggarakan oleh Kementerian Pekerjaan Umum (PU) bersama pihak kontraktor pelaksana pembangunan. Acara peresmian turut dihadiri oleh Wali Kota Makassar serta masyarakat sekitar sebagai bentuk apresiasi atas hadirnya fasilitas ini di tengah lingkungan pendidikan Kota Makassar.
